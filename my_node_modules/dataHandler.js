const fs = require('fs');

// utility fnction: reading json and returning object corresponding to it
function readData() {
    const jsonData = fs.readFileSync('../serverDb.json');
    return JSON.parse(jsonData);
}


const getAllFoods = () => readData();



function getFoodNames() {
    const data = readData();
    return data.map(item => item['Food (100g)']);
}


module.exports = { getAllFoods, getFoodNames };